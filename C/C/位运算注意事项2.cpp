#include<stdio.h>
#include<stdlib.h>
int main()
{
	char a =-3;//1000 0011原码
	int b =-12;//1000 0000 0000 0000 0000 0000 0000 1100
	printf("%d\n %d\n %d\n",a&b,a|b,a^b);
	//低字节向高字节类型进行转变的时候，会自动填充0；
	//1111 1111 1111 1111 1111 1111 1111 1101(-3的补码)
	//1111 1111 1111 1111 1111 1111 1111 0100(-12的补码)

	//位运算的~,&,|,^,运算符无视最高位的正负表示符号
	//运算时会作为正常0和1来处理

	getchar();
}