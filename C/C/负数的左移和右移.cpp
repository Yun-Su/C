#include<stdio.h>
#include<stdlib.h>
int main()
{
	int a = -63;         //1111 1111 1111 1111 1111 1111 1100 0001
	unsigned int b = 127;//0000 0000 0000 0000 0000 0001 001111 11
	printf("%d\n%d\n", a <<= 2, b <<= 2);
	//1111 1111 1111 1111 1111 1111 1100 0001//
	//1111 1111 1111 1111 1111 1111 0000 0100//(-252的补码)
	//前移动两位右边增加对应位置的0
	printf("%d,%d\n",a>>2,b>>2);
	//此处同理，注意二者移位变化，最高位符号位不变

	getchar();
}